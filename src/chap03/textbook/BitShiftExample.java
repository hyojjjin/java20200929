package chap03.textbook;

public class BitShiftExample {
	public static void main(String[] args) {
		System.out.println("1 << 3 = " + (1 << 3));
		System.out.println("-8 >> 3 = " + (-8 >> 3));
		System.out.println("-8 >>> 3 = " + (-8 >>> 3));
		
		//1 0001
		//3 0011
		//-8 1111 1111 1111 1111 1111 1111 1111 1000
		
		
		//1 << 3 => 8 = + 8 //8
		// 
				
	}
}
